def beautify_axis(
    ax: plt.Axes,
    xlabel: str = "",
    ylabel: str = "",
    title: str = "",
    rotate_x: int = 0,
) -> None:
    """Apply consistent axis formatting."""

    ax.set_xlabel(xlabel, fontsize=10.5, fontweight="bold")
    ax.set_ylabel(ylabel, fontsize=10.5, fontweight="bold")
    ax.set_title(title, fontsize=11.5, fontweight="bold", pad=8)
    ax.tick_params(axis="both", labelsize=9, width=1.0, length=4)

    for tick in ax.get_xticklabels():
        tick.set_rotation(rotate_x)
        tick.set_fontweight("bold")

    for tick in ax.get_yticklabels():
        tick.set_fontweight("bold")

    for spine in ax.spines.values():
        spine.set_linewidth(1.2)

    ax.grid(
        axis="y",
        linestyle="--",
        linewidth=0.6,
        alpha=0.28,
    )


def safe_compositional_normalisation(values: np.ndarray) -> np.ndarray:
    """Normalise a non-negative vector to one and reject invalid vectors."""

    values = np.asarray(values, dtype=float)
    values = np.clip(values, 0.0, None)

    total = float(values.sum())

    if not np.isfinite(total) or total <= 0:
        raise ValueError(
            "A compositional vector has a non-positive or non-finite sum."
        )

    return values / total


def choose_representative_samples(
    prediction_df: pd.DataFrame,
) -> list[str]:
    """
    Select best, median and worst Random Forest samples according to
    mean Bray-Curtis distance across shallow-sequencing repeats.
    """

    sample_records = []

    for sample_id, sample_data in prediction_df.groupby(
        "sample_id",
        observed=True,
        sort=False,
    ):
        repeat_distances = []

        for _, repeat_data in sample_data.groupby(
            "subsample_repeat",
            observed=True,
            sort=False,
        ):
            repeat_data = repeat_data.loc[
                repeat_data["otu_id"].astype(str) != "OTHER"
            ].copy()

            true_values = safe_compositional_normalisation(
                repeat_data["target_reference_ra"].to_numpy(dtype=float)
            )

            rf_values = safe_compositional_normalisation(
                repeat_data["pred_random_forest_ra"].to_numpy(dtype=float)
            )

            bray_curtis = 0.5 * np.sum(
                np.abs(true_values - rf_values)
            )

            repeat_distances.append(float(bray_curtis))

        if repeat_distances:
            sample_records.append(
                {
                    "sample_id": str(sample_id),
                    "rf_mean_bray_curtis": float(
                        np.mean(repeat_distances)
                    ),
                }
            )

    sample_performance = (
        pd.DataFrame(sample_records)
        .sort_values(
            "rf_mean_bray_curtis",
            ascending=True,
        )
        .reset_index(drop=True)
    )

    if sample_performance.empty:
        raise ValueError(
            "No eligible samples were available for RAD selection."
        )

    if len(sample_performance) < 3:
        return sample_performance["sample_id"].tolist()

    return [
        str(sample_performance.iloc[0]["sample_id"]),
        str(
            sample_performance.iloc[
                len(sample_performance) // 2
            ]["sample_id"]
        ),
        str(sample_performance.iloc[-1]["sample_id"]),
    ]


def get_one_repeat_sample(
    prediction_df: pd.DataFrame,
    sample_id: str,
    repeat_id: int = RAD_REPEAT_ID,
) -> pd.DataFrame:
    """Return one shallow repeat for one biological sample."""

    sample_ids_as_text = prediction_df["sample_id"].astype(str)

    current = prediction_df.loc[
        sample_ids_as_text == str(sample_id)
    ].copy()

    if current.empty:
        raise ValueError(
            f"sample_id was not found in the prediction table: {sample_id}"
        )

    available_repeats = sorted(
        current["subsample_repeat"].dropna().unique().tolist()
    )

    if not available_repeats:
        raise ValueError(
            f"No shallow repeats were found for sample_id: {sample_id}"
        )

    selected_repeat = (
        repeat_id
        if repeat_id in available_repeats
        else available_repeats[0]
    )

    current = current.loc[
        current["subsample_repeat"] == selected_repeat
    ].copy()

    current = current.loc[
        current["otu_id"].astype(str) != "OTHER"
    ].copy()

    if current.empty:
        raise ValueError(
            f"No specific OTU rows remained for sample_id: {sample_id}"
        )

    return current


def extract_improvement_values(
    data: pd.DataFrame,
    group_column: str,
    group_value: str,
    metric_column: str,
    metric_value: str,
    method_column: str,
    method_value: str,
    value_column: str,
) -> float:
    """Extract one improvement value or return NaN when it is unavailable."""

    selected = data.loc[
        (data[group_column] == group_value)
        & (data[metric_column] == metric_value)
        & (data[method_column] == method_value),
        value_column,
    ]

    if selected.empty:
        return np.nan

    return float(selected.iloc[0])


