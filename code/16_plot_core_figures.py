def prepare_abundance_improvement_table(
    abundance_improvements: pd.DataFrame,
) -> tuple[pd.DataFrame, str, str, str, str]:
    """Resolve accepted Stage-12 column names for abundance improvements."""

    group_column = first_existing_column(
        abundance_improvements,
        ["abundance_group", "reference_abundance_group"],
        "12_abundance_group_improvement_vs_raw.csv",
    )

    method_column = first_existing_column(
        abundance_improvements,
        ["model", "method"],
        "12_abundance_group_improvement_vs_raw.csv",
    )

    metric_column = first_existing_column(
        abundance_improvements,
        ["metric"],
        "12_abundance_group_improvement_vs_raw.csv",
    )

    value_column = first_existing_column(
        abundance_improvements,
        [
            "relative_improvement_percent",
            "improvement_percent",
            "relative_improvement_percent_vs_raw",
        ],
        "12_abundance_group_improvement_vs_raw.csv",
    )

    output = normalise_method_names(
        abundance_improvements,
        method_column,
    )

    output = normalise_abundance_groups(
        output,
        group_column,
    )

    return (
        output,
        group_column,
        method_column,
        metric_column,
        value_column,
    )


