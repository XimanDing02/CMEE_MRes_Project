def print_step(step_number: int, message: str) -> None:
    """Print one formatted progress heading."""

    print("\n" + "=" * 92)
    print(f"Step {step_number}: {message}")
    print("=" * 92)


def require_file(file_path: Path) -> None:
    """Raise a clear error when one required input file is missing."""

    if not file_path.is_file():
        raise FileNotFoundError(
            "Required input file was not found:\n"
            f"{file_path}\n"
            "Run scripts 11, 12 and 13 before running this script."
        )


def require_columns(
    data: pd.DataFrame,
    required_columns: Iterable[str],
    table_name: str,
) -> None:
    """Raise a clear error when a table lacks required columns."""

    missing_columns = [
        column
        for column in required_columns
        if column not in data.columns
    ]

    if missing_columns:
        raise KeyError(
            f"{table_name} is missing required columns:\n"
            f"{missing_columns}\n"
            f"Available columns:\n{data.columns.tolist()}"
        )


def first_existing_column(
    data: pd.DataFrame,
    candidates: Sequence[str],
    table_name: str,
) -> str:
    """Return the first available column from a list of accepted names."""

    for column in candidates:
        if column in data.columns:
            return column

    raise KeyError(
        f"{table_name} does not contain any accepted column name from:\n"
        f"{list(candidates)}\n"
        f"Available columns:\n{data.columns.tolist()}"
    )


