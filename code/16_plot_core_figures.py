def plot_recovery_improvements(
    abundance_improvements: pd.DataFrame,
    zero_improvements: pd.DataFrame,
) -> None:
    """Plot zero-recovery and abundance-stratified improvements."""

    print_step(
        6,
        "Create Figure 5: zero and abundance-group recovery",
    )

    (
        abundance_data,
        abundance_group_column,
        abundance_method_column,
        abundance_metric_column,
        abundance_value_column,
    ) = prepare_abundance_improvement_table(
        abundance_improvements
    )

    (
        zero_data,
        zero_method_column,
        zero_metric_column,
        zero_value_column,
    ) = prepare_zero_improvement_table(
        zero_improvements
    )

    fig, axes = plt.subplots(
        1,
        3,
        figsize=(17.0, 5.4),
    )

    bar_width = 0.34

    # Panel A: shallow-zero recovery
    ax = axes[0]
    x_positions = np.arange(len(ZERO_METRIC_ORDER))

    rf_values = []
    xgb_values = []

    for metric in ZERO_METRIC_ORDER:
        rf_values.append(
            extract_improvement_values(
                data=zero_data,
                group_column=zero_metric_column,
                group_value=metric,
                metric_column=zero_metric_column,
                metric_value=metric,
                method_column=zero_method_column,
                method_value="Random Forest",
                value_column=zero_value_column,
            )
        )

        xgb_values.append(
            extract_improvement_values(
                data=zero_data,
                group_column=zero_metric_column,
                group_value=metric,
                metric_column=zero_metric_column,
                metric_value=metric,
                method_column=zero_method_column,
                method_value="XGBoost",
                value_column=zero_value_column,
            )
        )

    ax.bar(
        x_positions - bar_width / 2,
        rf_values,
        width=bar_width,
        color=METHOD_COLORS["Random Forest"],
        edgecolor="black",
        linewidth=0.9,
        label="RF",
    )

    ax.bar(
        x_positions + bar_width / 2,
        xgb_values,
        width=bar_width,
        color=METHOD_COLORS["XGBoost"],
        edgecolor="black",
        linewidth=0.9,
        label="XGB",
    )

    ax.axhline(
        0,
        color="black",
        linestyle="--",
        linewidth=1.0,
    )

    ax.set_xticks(x_positions)
    ax.set_xticklabels(
        [ZERO_METRIC_LABELS[metric] for metric in ZERO_METRIC_ORDER]
    )

    beautify_axis(
        ax,
        xlabel="Metric",
        ylabel="Improvement over Raw (%)",
        title="Zero-recovery improvement",
    )

    ax.legend(
        frameon=False,
        fontsize=9,
    )

    # Panels B-C: abundance-group improvements
    abundance_metric_candidates = [
        (
            ["RMSE_RA", "composition_RMSE_RA"],
            "Abundance-stratified improvement\nRMSE (RA scale)",
        ),
        (
            ["RMSE_log10_RA", "RMSE_log10"],
            "Abundance-stratified improvement\nRMSE (log10 scale)",
        ),
    ]

    available_abundance_metrics = set(
        abundance_data[abundance_metric_column]
        .dropna()
        .astype(str)
        .tolist()
    )

    for ax, (metric_candidates, panel_title) in zip(
        axes[1:],
        abundance_metric_candidates,
    ):
        selected_metric = next(
            (
                metric
                for metric in metric_candidates
                if metric in available_abundance_metrics
            ),
            None,
        )

        if selected_metric is None:
            raise ValueError(
                "The abundance-improvement table does not contain an "
                f"accepted metric from: {metric_candidates}\n"
                f"Available metrics: {sorted(available_abundance_metrics)}"
            )

        x_positions = np.arange(len(ABUNDANCE_ORDER))

        rf_values = []
        xgb_values = []

        for abundance_group in ABUNDANCE_ORDER:
            rf_values.append(
                extract_improvement_values(
                    data=abundance_data,
                    group_column=abundance_group_column,
                    group_value=abundance_group,
                    metric_column=abundance_metric_column,
                    metric_value=selected_metric,
                    method_column=abundance_method_column,
                    method_value="Random Forest",
                    value_column=abundance_value_column,
                )
            )

            xgb_values.append(
                extract_improvement_values(
                    data=abundance_data,
                    group_column=abundance_group_column,
                    group_value=abundance_group,
                    metric_column=abundance_metric_column,
                    metric_value=selected_metric,
                    method_column=abundance_method_column,
                    method_value="XGBoost",
                    value_column=abundance_value_column,
                )
            )

        ax.bar(
            x_positions - bar_width / 2,
            rf_values,
            width=bar_width,
            color=METHOD_COLORS["Random Forest"],
            edgecolor="black",
            linewidth=0.9,
            label="RF",
        )

        ax.bar(
            x_positions + bar_width / 2,
            xgb_values,
            width=bar_width,
            color=METHOD_COLORS["XGBoost"],
            edgecolor="black",
            linewidth=0.9,
            label="XGB",
        )

        ax.axhline(
            0,
            color="black",
            linestyle="--",
            linewidth=1.0,
        )

        ax.set_xticks(x_positions)
        ax.set_xticklabels(
            [
                ABUNDANCE_SHORT[group]
                for group in ABUNDANCE_ORDER
            ],
            rotation=18,
            ha="right",
        )

        beautify_axis(
            ax,
            xlabel="Reference abundance group",
            ylabel="Improvement over Raw (%)",
            title=panel_title,
        )

    fig.suptitle(
        "Figure 5. Zero-recovery and abundance-stratified improvements",
        fontsize=15,
        fontweight="bold",
        y=0.995,
    )

    fig.tight_layout(rect=(0, 0, 1, 0.94))

    save_figure(
        fig,
        "Figure5_zero_recovery_and_abundance_combined",
    )
    plt.close(fig)


