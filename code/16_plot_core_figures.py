def plot_rad_examples(
    test_predictions: pd.DataFrame,
) -> None:
    """Plot RAD reconstruction for best, median and worst RF samples."""

    print_step(
        5,
        "Create Figure 4: representative RAD reconstructions",
    )

    representative_samples = choose_representative_samples(
        test_predictions
    )

    sample_roles = [
        "Best RF sample",
        "Median RF sample",
        "Worst RF sample",
    ]

    panel_count = len(representative_samples)

    fig, axes_object = plt.subplots(
        1,
        panel_count,
        figsize=(5.4 * panel_count, 5.2),
        squeeze=False,
    )

    axes = axes_object.ravel()

    for ax, sample_id, sample_role in zip(
        axes,
        representative_samples,
        sample_roles,
    ):
        current = get_one_repeat_sample(
            prediction_df=test_predictions,
            sample_id=sample_id,
            repeat_id=RAD_REPEAT_ID,
        )

        true_ra = safe_compositional_normalisation(
            current["target_reference_ra"].to_numpy(dtype=float)
        )
        raw_ra = safe_compositional_normalisation(
            current["pred_raw_shallow_ra"].to_numpy(dtype=float)
        )
        rf_ra = safe_compositional_normalisation(
            current["pred_random_forest_ra"].to_numpy(dtype=float)
        )
        xgb_ra = safe_compositional_normalisation(
            current["pred_xgboost_ra"].to_numpy(dtype=float)
        )

        rank = np.arange(1, len(true_ra) + 1)

        rad_series = [
            (
                "Reference",
                np.sort(true_ra)[::-1],
                "black",
                "-",
                2.0,
            ),
            (
                "Raw",
                np.sort(raw_ra)[::-1],
                METHOD_COLORS["Raw shallow RA"],
                "--",
                1.7,
            ),
            (
                "RF",
                np.sort(rf_ra)[::-1],
                METHOD_COLORS["Random Forest"],
                "-",
                1.8,
            ),
            (
                "XGB",
                np.sort(xgb_ra)[::-1],
                METHOD_COLORS["XGBoost"],
                "-.",
                1.7,
            ),
        ]

        for label, values, color, linestyle, linewidth in rad_series:
            ax.plot(
                rank,
                np.log10(values + PSEUDO_COUNT),
                color=color,
                linestyle=linestyle,
                linewidth=linewidth,
                label=label,
            )

        ax.set_xscale("log")

        beautify_axis(
            ax,
            xlabel="Rank (log scale)",
            ylabel="log10(Relative abundance + 1e-8)",
            title=f"{sample_role}\nsample_id = {sample_id}",
        )

    axes[0].legend(
        frameon=False,
        fontsize=9,
        loc="upper right",
    )

    fig.suptitle(
        "Figure 4. Representative rank-abundance reconstructions",
        fontsize=15,
        fontweight="bold",
        y=0.995,
    )

    fig.tight_layout(rect=(0, 0, 1, 0.94))

    save_figure(
        fig,
        "Figure4_RAD_examples_combined",
    )
    plt.close(fig)


