def plot_primary_metrics(
    sample_level_metrics: pd.DataFrame,
) -> None:
    """Plot distributions of the six primary test metrics."""

    print_step(2, "Create Figure 1: primary test metrics")

    fig, axes = plt.subplots(
        2,
        3,
        figsize=(15.8, 9.5),
    )

    for ax, metric in zip(axes.flat, PRIMARY_METRICS):
        plot_data = sample_level_metrics.loc[
            sample_level_metrics["method"].isin(METHOD_ORDER),
            ["sample_id", "method", metric],
        ].dropna(subset=[metric])

        sns.boxplot(
            data=plot_data,
            x="method",
            y=metric,
            order=METHOD_ORDER,
            palette=METHOD_COLORS,
            width=0.62,
            linewidth=1.1,
            fliersize=0,
            ax=ax,
        )

        sns.stripplot(
            data=plot_data,
            x="method",
            y=metric,
            order=METHOD_ORDER,
            color="black",
            size=2.2,
            jitter=0.20,
            alpha=0.36,
            ax=ax,
        )

        ax.set_xticks(range(len(METHOD_ORDER)))
        ax.set_xticklabels(
            [METHOD_SHORT[method] for method in METHOD_ORDER]
        )

        beautify_axis(
            ax,
            xlabel="Method",
            ylabel=METRIC_LABELS[metric],
            title=METRIC_LABELS[metric],
        )

    fig.suptitle(
        "Figure 1. Test-sample performance across primary metrics",
        fontsize=15,
        fontweight="bold",
        y=0.995,
    )

    fig.tight_layout(rect=(0, 0, 1, 0.97))

    save_figure(
        fig,
        "Figure1_primary_metrics_combined",
    )
    plt.close(fig)


