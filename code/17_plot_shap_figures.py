def select_case_row(
    local_cases: pd.DataFrame,
    model_name: str,
    preferred_case: str,
) -> pd.Series:
    """Select one preferred local case for a model, with a safe fallback."""

    current = local_cases.loc[local_cases["model"] == model_name].copy()
    preferred = current.loc[current["local_case_type"] == preferred_case]

    if not preferred.empty:
        return preferred.iloc[0]

    if current.empty:
        raise ValueError(f"No local SHAP cases were found for {model_name}.")

    warnings.warn(
        f"Preferred case '{preferred_case}' was not found for {model_name}; "
        "the first available case will be used."
    )
    return current.iloc[0]


def plot_local_contributions(
    ax: plt.Axes,
    row: pd.Series,
    model_name: str,
    case_title: str,
    n_top: int = 7,
) -> None:
    """Plot the leading positive and negative SHAP contributions for one row."""

    contributions: list[dict[str, object]] = []

    for feature in FEATURE_COLUMNS:
        shap_column = f"SHAP__{feature}"

        if shap_column not in row.index or pd.isna(row[shap_column]):
            continue

        contributions.append(
            {
                "feature": feature,
                "label": FEATURE_LABELS.get(feature, feature),
                "shap_value": float(row[shap_column]),
            }
        )

    contribution_df = pd.DataFrame(contributions)

    if contribution_df.empty:
        raise ValueError(
            f"No SHAP contribution columns were available for {model_name}."
        )

    contribution_df["abs_shap"] = contribution_df["shap_value"].abs()
    contribution_df = contribution_df.sort_values("abs_shap", ascending=False)
    top_df = contribution_df.head(n_top).copy()
    remaining = contribution_df.iloc[n_top:]

    if not remaining.empty:
        other_value = float(remaining["shap_value"].sum())
        top_df = pd.concat(
            [
                top_df,
                pd.DataFrame(
                    {
                        "feature": ["other_features"],
                        "label": ["Other features"],
                        "shap_value": [other_value],
                        "abs_shap": [abs(other_value)],
                    }
                ),
            ],
            ignore_index=True,
        )

    top_df = top_df.sort_values("shap_value", ascending=True)
    colours = [
        "#D65F5F" if value > 0 else "#3B82B4"
        for value in top_df["shap_value"]
    ]

    ax.barh(
        np.arange(len(top_df)),
        top_df["shap_value"],
        color=colours,
        edgecolor="black",
        linewidth=0.85,
        alpha=0.90,
    )
    ax.set_yticks(np.arange(len(top_df)))
    ax.set_yticklabels(top_df["label"], fontsize=8.9, fontweight="bold")
    ax.axvline(0, color="black", linewidth=1.1)

    reference_ra = float(row["target_reference_ra"])
    raw_ra = float(row["pred_raw_shallow_ra"])

    if model_name == "Random Forest":
        model_ra = float(row["pred_random_forest_ra"])
    else:
        model_ra = float(row["pred_xgboost_ra"])

    subtitle = (
        f"{case_title}\n"
        f"Reference RA={reference_ra:.3e}, "
        f"Raw={raw_ra:.3e}, "
        f"Model={model_ra:.3e}"
    )

    beautify_axis(
        ax=ax,
        xlabel="SHAP contribution to log10 prediction",
        title=f"{model_name}\n{subtitle}",
        grid_axis="x",
    )


