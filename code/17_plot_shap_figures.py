def read_shap_outputs() -> tuple[
    pd.DataFrame,
    pd.DataFrame,
    pd.DataFrame,
    pd.DataFrame,
    pd.DataFrame,
]:
    """Read and validate all SHAP files needed for plotting."""

    print("=" * 88)
    print("Step 1: Read and validate SHAP outputs")
    print("=" * 88)

    for required_file in [
        RF_SHAP_FILE,
        XGB_SHAP_FILE,
        GLOBAL_IMPORTANCE_FILE,
        SUBGROUP_IMPORTANCE_FILE,
        LOCAL_CASES_FILE,
    ]:
        require_file(required_file)

    rf_shap = pd.read_pickle(RF_SHAP_FILE)
    xgb_shap = pd.read_pickle(XGB_SHAP_FILE)
    global_importance = pd.read_csv(GLOBAL_IMPORTANCE_FILE)
    subgroup_importance = pd.read_csv(SUBGROUP_IMPORTANCE_FILE)
    local_cases = pd.read_csv(LOCAL_CASES_FILE)

    require_columns(
        global_importance,
        ["model", "feature", "mean_abs_SHAP"],
        "Global SHAP importance table",
    )
    require_columns(
        subgroup_importance,
        [
            "model",
            "subgroup_variable",
            "subgroup",
            "feature",
            "importance_percent",
        ],
        "Subgroup SHAP importance table",
    )
    require_columns(
        local_cases,
        [
            "model",
            "local_case_type",
            "target_reference_ra",
            "pred_raw_shallow_ra",
            "pred_random_forest_ra",
            "pred_xgboost_ra",
        ],
        "Local SHAP case table",
    )

    if ADDITIVITY_FILE.exists():
        additivity_check = pd.read_csv(ADDITIVITY_FILE)
        print("\nSHAP additivity check:")
        print(additivity_check.to_string(index=False))
    else:
        print("\nOptional additivity-check file was not found; plotting will continue.")

    print(f"\nRandom Forest SHAP table: {rf_shap.shape}")
    print(f"XGBoost SHAP table:      {xgb_shap.shape}")
    print(f"Global importance table: {global_importance.shape}")
    print(f"Subgroup importance:     {subgroup_importance.shape}")
    print(f"Local case table:         {local_cases.shape}")

    return (
        rf_shap,
        xgb_shap,
        global_importance,
        subgroup_importance,
        local_cases,
    )


