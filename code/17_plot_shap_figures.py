def plot_importance_bar(
    ax: plt.Axes,
    global_importance: pd.DataFrame,
    model_name: str,
    n_top: int,
) -> None:
    """Plot mean absolute SHAP importance for one model."""

    current = (
        global_importance.loc[global_importance["model"] == model_name]
        .sort_values("mean_abs_SHAP", ascending=False)
        .head(n_top)
        .copy()
        .iloc[::-1]
    )

    labels = [FEATURE_LABELS.get(feature, feature) for feature in current["feature"]]

    ax.barh(
        np.arange(len(current)),
        current["mean_abs_SHAP"],
        color=MODEL_COLORS[model_name],
        edgecolor="black",
        linewidth=0.9,
        alpha=0.90,
    )
    ax.set_yticks(np.arange(len(current)))
    ax.set_yticklabels(labels, fontsize=9.2, fontweight="bold")

    for index, value in enumerate(current["mean_abs_SHAP"]):
        ax.text(
            value,
            index,
            f"  {value:.3f}",
            va="center",
            ha="left",
            fontsize=8.5,
            fontweight="bold",
        )

    beautify_axis(
        ax=ax,
        xlabel="Mean |SHAP value|",
        title=f"{model_name}: global importance",
        grid_axis="x",
    )


def plot_custom_beeswarm(
    ax: plt.Axes,
    shap_table: pd.DataFrame,
    top_features: list[str],
    title: str,
    random_state: int,
) -> None:
    """Plot a compact SHAP beeswarm using percentile-scaled feature colours."""

    rng = np.random.default_rng(random_state)

    for feature_index, feature in enumerate(top_features):
        shap_column = f"SHAP__{feature}"

        if shap_column not in shap_table.columns or feature not in shap_table.columns:
            continue

        shap_values = (
            pd.to_numeric(shap_table[shap_column], errors="coerce")
            .fillna(0.0)
            .to_numpy(dtype=float)
        )
        colour_values = percentile_colour_values(shap_table[feature])
        jitter = rng.normal(loc=0.0, scale=0.105, size=len(shap_values))

        ax.scatter(
            shap_values,
            feature_index + jitter,
            c=colour_values,
            cmap="coolwarm",
            vmin=0,
            vmax=1,
            s=17,
            alpha=0.68,
            edgecolors="none",
            rasterized=True,
        )

    ax.axvline(0, color="black", linestyle="--", linewidth=1.1)
    ax.set_yticks(np.arange(len(top_features)))
    ax.set_yticklabels(
        [FEATURE_LABELS.get(feature, feature) for feature in top_features],
        fontsize=9.2,
        fontweight="bold",
    )
    ax.invert_yaxis()

    beautify_axis(
        ax=ax,
        xlabel="SHAP value",
        title=title,
        grid_axis="x",
    )


