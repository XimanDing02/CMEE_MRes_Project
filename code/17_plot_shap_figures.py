def require_file(file_path: Path) -> None:
    """Raise a clear error when a required input file is missing."""

    if not file_path.exists():
        raise FileNotFoundError(
            "Required SHAP file was not found:\n"
            f"{file_path}\n\n"
            "Please run 14_compute_shap_results.py first."
        )


def require_columns(
    table: pd.DataFrame,
    required_columns: list[str],
    table_name: str,
) -> None:
    """Check that a table contains all columns needed by this script."""

    missing = [column for column in required_columns if column not in table.columns]

    if missing:
        raise ValueError(
            f"{table_name} is missing required columns:\n"
            + "\n".join(f"  - {column}" for column in missing)
        )


