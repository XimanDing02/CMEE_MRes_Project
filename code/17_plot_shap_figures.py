def smooth_dependence_line(
    x: np.ndarray,
    y: np.ndarray,
) -> tuple[np.ndarray, np.ndarray]:
    """Create a LOWESS line, with a binned-median fallback."""

    x = np.asarray(x, dtype=float)
    y = np.asarray(y, dtype=float)
    valid = np.isfinite(x) & np.isfinite(y)
    x = x[valid]
    y = y[valid]

    if len(x) < 10:
        order = np.argsort(x)
        return x[order], y[order]

    if np.unique(x).size <= 2:
        unique_x = np.sort(np.unique(x))
        median_y = np.asarray([np.median(y[x == value]) for value in unique_x])
        return unique_x, median_y

    if HAS_LOWESS:
        smoothed = lowess(
            endog=y,
            exog=x,
            frac=0.30,
            it=0,
            return_sorted=True,
        )
        return smoothed[:, 0], smoothed[:, 1]

    edges = np.unique(np.quantile(x, np.linspace(0, 1, 21)))

    if len(edges) < 3:
        order = np.argsort(x)
        return x[order], y[order]

    bin_ids = np.digitize(x, edges[1:-1], right=False)
    smooth_x: list[float] = []
    smooth_y: list[float] = []

    for bin_id in np.unique(bin_ids):
        mask = bin_ids == bin_id

        if mask.sum() == 0:
            continue

        smooth_x.append(float(np.median(x[mask])))
        smooth_y.append(float(np.median(y[mask])))

    return np.asarray(smooth_x), np.asarray(smooth_y)


def plot_dependence(
    ax: plt.Axes,
    shap_table: pd.DataFrame,
    feature: str,
    model_name: str,
) -> None:
    """Plot one feature value against its SHAP contribution."""

    shap_column = f"SHAP__{feature}"

    if feature not in shap_table.columns or shap_column not in shap_table.columns:
        raise ValueError(
            f"Dependence plot columns are missing for feature: {feature}"
        )

    x = pd.to_numeric(shap_table[feature], errors="coerce").to_numpy(dtype=float)
    y = pd.to_numeric(shap_table[shap_column], errors="coerce").to_numpy(dtype=float)
    valid = np.isfinite(x) & np.isfinite(y)
    x = x[valid]
    y = y[valid]

    ax.scatter(
        x,
        y,
        s=22,
        alpha=0.48,
        color=MODEL_COLORS[model_name],
        edgecolors="none",
        rasterized=True,
    )

    smooth_x, smooth_y = smooth_dependence_line(x, y)
    order = np.argsort(smooth_x)
    ax.plot(smooth_x[order], smooth_y[order], color="black", linewidth=2.0)
    ax.axhline(0, color="black", linestyle="--", linewidth=1.0, alpha=0.75)

    if len(x) > 2 and np.unique(x).size > 1 and np.unique(y).size > 1:
        rho, _ = spearmanr(x, y)
    else:
        rho = np.nan

    ax.text(
        0.04,
        0.95,
        f"Spearman rho = {rho:.3f}",
        transform=ax.transAxes,
        ha="left",
        va="top",
        fontsize=9.2,
        fontweight="bold",
        bbox={
            "boxstyle": "round,pad=0.25",
            "facecolor": "white",
            "edgecolor": "black",
            "alpha": 0.88,
        },
    )

    label = FEATURE_LABELS.get(feature, feature)
    beautify_axis(
        ax=ax,
        xlabel=label,
        ylabel="SHAP value",
        title=label,
        grid_axis="both",
    )


