def build_subgroup_matrix(
    subgroup_importance: pd.DataFrame,
    model_name: str,
    subgroup_variable: str,
    subgroup_order: list[str],
    top_features: list[str],
) -> pd.DataFrame:
    """Build one subgroup-by-feature SHAP importance matrix."""

    current = subgroup_importance.loc[
        (subgroup_importance["model"] == model_name)
        & (subgroup_importance["subgroup_variable"] == subgroup_variable)
        & (subgroup_importance["feature"].isin(top_features))
    ].copy()

    matrix = current.pivot_table(
        index="subgroup",
        columns="feature",
        values="importance_percent",
        aggfunc="mean",
    )
    matrix = matrix.reindex(index=subgroup_order, columns=top_features)
    matrix.columns = [FEATURE_LABELS.get(feature, feature) for feature in matrix.columns]

    return matrix


def plot_subgroup_heatmaps(
    subgroup_importance: pd.DataFrame,
    rf_top_features: list[str],
    xgb_top_features: list[str],
) -> None:
    """Create abundance-group and shallow-detection SHAP heatmaps."""

    print("\n" + "=" * 88)
    print("Step 3: Plot subgroup SHAP importance heatmaps")
    print("=" * 88)

    combined_top_features: list[str] = []

    for feature in rf_top_features + xgb_top_features:
        if feature not in combined_top_features:
            combined_top_features.append(feature)

    combined_top_features = combined_top_features[:N_TOP_FEATURES]

    heatmap_matrices: dict[tuple[str, str], pd.DataFrame] = {}

    for model_name in MODEL_ORDER:
        heatmap_matrices[(model_name, "abundance_group")] = build_subgroup_matrix(
            subgroup_importance,
            model_name,
            "abundance_group",
            ABUNDANCE_ORDER,
            combined_top_features,
        )
        heatmap_matrices[(model_name, "shallow_detection_group")] = (
            build_subgroup_matrix(
                subgroup_importance,
                model_name,
                "shallow_detection_group",
                DETECTION_ORDER,
                combined_top_features,
            )
        )

    all_values = np.concatenate(
        [matrix.to_numpy(dtype=float).ravel() for matrix in heatmap_matrices.values()]
    )
    all_values = all_values[np.isfinite(all_values)]

    common_vmax = (
        float(np.nanpercentile(all_values, 98)) if len(all_values) > 0 else 50.0
    )
    common_vmax = max(common_vmax, 1.0)

    fig, axes = plt.subplots(2, 2, figsize=(17.0, 10.0))

    heatmap_specs = [
        (
            "Random Forest",
            "abundance_group",
            axes[0, 0],
            "Random Forest: abundance strata",
        ),
        (
            "XGBoost",
            "abundance_group",
            axes[0, 1],
            "XGBoost: abundance strata",
        ),
        (
            "Random Forest",
            "shallow_detection_group",
            axes[1, 0],
            "Random Forest: shallow detection",
        ),
        (
            "XGBoost",
            "shallow_detection_group",
            axes[1, 1],
            "XGBoost: shallow detection",
        ),
    ]

    for panel_index, (model_name, subgroup_variable, ax, title) in enumerate(
        heatmap_specs
    ):
        matrix = heatmap_matrices[(model_name, subgroup_variable)].copy()

        if subgroup_variable == "abundance_group":
            matrix.index = [
                ABUNDANCE_LABELS.get(item, item) for item in matrix.index
            ]

        sns.heatmap(
            matrix,
            cmap="YlOrRd",
            vmin=0,
            vmax=common_vmax,
            annot=True,
            fmt=".1f",
            linewidths=0.7,
            linecolor="white",
            cbar=(panel_index == 3),
            cbar_kws={"label": "Relative SHAP importance (%)"},
            ax=ax,
        )

        ax.set_xlabel("Feature", fontsize=11, fontweight="bold")
        ax.set_ylabel("Subgroup", fontsize=11, fontweight="bold")
        ax.set_title(title, fontsize=12, fontweight="bold", pad=9)
        ax.tick_params(axis="x", labelrotation=25, labelsize=8.8)
        ax.tick_params(axis="y", labelrotation=0, labelsize=9.2)

        for tick in ax.get_xticklabels():
            tick.set_ha("right")
            tick.set_fontweight("bold")

        for tick in ax.get_yticklabels():
            tick.set_fontweight("bold")

        add_panel_label(ax, PANEL_LABELS[panel_index])

    fig.suptitle(
        "SHAP importance across abundance and detection subgroups",
        fontsize=16,
        fontweight="bold",
        y=1.01,
    )
    fig.tight_layout()

    save_figure(fig, "17_subgroup_shap_heatmaps")
    plt.close(fig)

    print("Saved: 17_subgroup_shap_heatmaps.[png|pdf|svg]")


