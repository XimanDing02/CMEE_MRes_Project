def beautify_axis(
    ax: plt.Axes,
    xlabel: str = "",
    ylabel: str = "",
    title: str = "",
    grid_axis: str = "x",
) -> None:
    """Apply a consistent publication-style format to one axis."""

    ax.set_xlabel(xlabel, fontsize=11, fontweight="bold")
    ax.set_ylabel(ylabel, fontsize=11, fontweight="bold")
    ax.set_title(title, fontsize=12, fontweight="bold", pad=9)
    ax.tick_params(axis="both", labelsize=9.5, width=1.1, length=4)

    for tick in ax.get_xticklabels():
        tick.set_fontweight("bold")

    for tick in ax.get_yticklabels():
        tick.set_fontweight("bold")

    for spine in ax.spines.values():
        spine.set_linewidth(1.3)

    if grid_axis in {"x", "y", "both"}:
        ax.grid(
            axis=grid_axis,
            linestyle="--",
            linewidth=0.65,
            alpha=0.28,
        )


def add_panel_label(ax: plt.Axes, label: str) -> None:
    """Add a bold panel label such as A, B, C, or D."""

    ax.text(
        -0.12,
        1.06,
        label,
        transform=ax.transAxes,
        fontsize=14,
        fontweight="bold",
        va="top",
        ha="left",
    )


def get_top_features(
    global_importance: pd.DataFrame,
    model_name: str,
    n_top: int,
) -> list[str]:
    """Return the leading SHAP features for one model."""

    current = (
        global_importance.loc[global_importance["model"] == model_name]
        .sort_values("mean_abs_SHAP", ascending=False)
    )

    features = current["feature"].head(n_top).tolist()

    if not features:
        raise ValueError(f"No SHAP importance rows were found for {model_name}.")

    return features


def percentile_colour_values(values: pd.Series) -> np.ndarray:
    """Convert feature values to percentile ranks between zero and one."""

    numeric = pd.to_numeric(values, errors="coerce")

    if numeric.notna().sum() == 0:
        return np.full(len(values), 0.5)

    numeric = numeric.fillna(numeric.median())

    if numeric.nunique() <= 1:
        return np.full(len(values), 0.5)

    return numeric.rank(method="average", pct=True).to_numpy(dtype=float)


